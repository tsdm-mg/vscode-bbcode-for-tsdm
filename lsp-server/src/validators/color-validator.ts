export function colorValidator(value: string): DiagnosticError[] {
  const colorRe = /^(#)?[0-9a-fA-F]{1,6}$/
  const colorMatch = colorRe.exec(value)
  if (colorMatch !== null) {
    return []
  }

  if (colorNames.has(value.toLowerCase())) {
    return []
  }

  return [DiagErr.invalidColor(value)]
}

export function colorToRgba(color: string | undefined): Color | undefined {
  if (color === undefined) {
    return
  }

  return colorNameToRgba.get(color) ?? hexToColor(color)
}

